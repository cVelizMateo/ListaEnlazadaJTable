    public void ActualizarTablaSalsa(){
        //Sección 1 
       
        //Sección 2

        String [] fila=new String[1]; 

        //Sección 3
        if(this.salsa!=null){
            salsa.irNodoSiguiente();
            Nodo auxiliar;
            auxiliar = salsa.primero;
            
            while(auxiliar!=null){
            fila[0]=auxiliar.Cancion;
            
         //Sección 4

         this.modeloSalsa.addRow(fila); 
        auxiliar = auxiliar.siguiente;
            }  
        }
         
         
        //Sección 5

        this.tblSalsa.setModel(modeloSalsa); 
}
    
