    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // aqui
        btnAgrupado.add(chRock);
        btnAgrupado.add(chSalsa);
        
        if(jTextFieldCancion.getText().length()>0){
            if(chRock.isSelected()){
                this.general.insertar(jTextFieldCancion.getText(), 2);
                this.rock.insertar(jTextFieldCancion.getText(),2);
                ActualizarTablaRock();
               
                ActualizarTablaGeneral();
                
                BorrarTextfield();
                
            }else if(chSalsa.isSelected()){
                
                this.general.insertar(jTextFieldCancion.getText(), 0);
                this.salsa.insertar(jTextFieldCancion.getText(),0);
                BorrarTextfield();
                ActualizarTablaSalsa();
                ActualizarTablaGeneral();
             
            }else{
            JOptionPane.showMessageDialog(null, "Por favor elegir genero de la canción ");
            
        }
           
        }else{
            
            JOptionPane.showMessageDialog(null, "Por favor ingresar nombre de la canción ");
            
        }
            
    }//GEN-LAST:event_btnAgregarActionPerformed

